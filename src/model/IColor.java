package model;

/**
 * Interface representing the RGB color of a pixel.
 */
public interface IColor {
  /**
   * Returns the red value of this color.
   *
   * @return red value.
   */
  int getRed();

  /**
   * Returns the green value of the color.
   *
   * @return green value.
   */
  int getGreen();

  /**
   * Returns the blue value of the color.
   *
   * @return blue value.
   */
  int getBlue();


}
